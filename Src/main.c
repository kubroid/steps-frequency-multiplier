// uses to prepare vars values before start
void static inline prepare_vars(void)
{
  // set individual wait timers for each axis
  for ( uint8_t axis = AXES; axis--; )
  {
    inp_last_time[axis] = PULSE_WAIT_TIME;
  }
}

// uses to get input pins states before start
void static inline get_input_pins_state(void)
{
  #define cur  dir_input_state_cur[axis]
  #define prev dir_input_state_prev[axis]
  for ( uint8_t axis = AXES; axis--; )
  {
    // get current state of a pin
    cur = read_pin(DIR_INPUTS[axis].PORT, DIR_INPUTS[axis].PIN);
    // save current state of a pin
    prev = cur;
  }
  #undef cur
  #undef prev
}

// uses to get output pins states before start
void static inline get_output_pins_state(void)
{
  #define step_cur  step_output_state_cur[axis]
  #define dir_cur   dir_output_state_cur[axis]
  for ( uint8_t axis = AXES; axis--; )
  {
    // get current state of the output pins
    step_cur = read_pin(STEP_OUTPUTS[axis].PORT, STEP_OUTPUTS[axis].PIN);
    dir_cur  = read_pin(DIR_OUTPUTS[axis].PORT, DIR_OUTPUTS[axis].PIN);
    // TODO - TEST CODE - remove this code
    _TEST_axis_step_out_pos[axis] = dir_cur;
  }
  #undef step_cur
  #undef dir_cur
}
